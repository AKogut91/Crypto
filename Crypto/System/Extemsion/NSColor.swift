import Cocoa

extension NSColor {
    
    static func percentGreen() -> NSColor {
        return NSColor(red: 127/255.0, green: 206/255.0, blue: 8/255.0, alpha: 1.0)
    }
    
    static func percentRed() -> NSColor {
        return NSColor(red: 196/255.0, green: 42/255.0, blue: 46/255.0, alpha: 1.0)
    }

}
